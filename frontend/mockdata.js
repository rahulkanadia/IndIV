// --- HELPER FUNCTION ---
export function getGlobalIVRange() {
    const allIVs = [
        ...mockData.skew.call,
        ...mockData.skew.put,
        ...mockData.skewMo.call,
        ...mockData.skewMo.put,
        ...mockData.term.weekly,
        ...mockData.term.monthly
    ];

    const minV = Math.min(...allIVs);
    const maxV = Math.max(...allIVs);

    return [Math.floor(minV) - 1, Math.ceil(maxV) + 1];
}
